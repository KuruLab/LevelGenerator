        fw.exportDataJSON(dir+"\\data_"+name+".json",
                "GraphStream", maxX, maxY, true);
        fw.exportMapJSON(dir+"\\map_"+name+".json",
                "Evolutionary"+BarabasiAlbertGenerator.class.getSimpleName(), true);
        
